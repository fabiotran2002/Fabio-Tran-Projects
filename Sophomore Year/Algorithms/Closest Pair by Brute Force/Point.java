

/**
 * Class to create point object 
 *
 * @author Fabio Tran
 * @version 1.0.0 2022-02-11 Initial implementation
 *
 */
public class Point
    {
    // instance variables
    public int x;
    public int y;
    
    /*
     * 2-arg constructor
     */
    public Point(int x, int y){
        this.x = x ;
        this.y = y ;
        
    } // end 2-arg constructor
    
    } // end class Point